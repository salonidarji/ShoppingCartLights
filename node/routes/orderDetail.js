var express = require("express");
var router = express.Router();
var OrderDetail = require("../models/orderDetailModel");

router.get("/:id?", function(req, res, next) {
  if (req.params.id) {
    OrderDetail.getOrderDetail(req.params.id, function(err, rows) {
      if (err) {
        res.json(err);
      } else {
        res.json(rows);
      }
    });
  } else {
    OrderDetail.getAllOrderDetail(function(err, rows) {
      if (err) {
        res.json(err);
      } else {
        res.json(rows);
      }
    });
  }
});
router.post("/", function(req, res, next) {
  OrderDetail.addOrderDetail(req.body, function(err, count) {
    if (err) {
      res.json(err);
    } else {
      res.json(req.body);
    }
  });
});
router.delete("/:id", function(req, res, next) {
  OrderDetail.deleteOrderDetail(req.params.id, function(err, count) {
    if (err) {
      res.json(err);
    } else {
      res.json(count);
    }
  });
});
router.put("/:id", function(req, res, next) {
  OrderDetail.updateStatus(req.params.id, req.body, function(err, rows) {
    if (err) {
      res.json(err);
    } else {
      res.json(rows);
    }
  });
});
module.exports = router;
